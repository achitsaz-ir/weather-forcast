/**
 * Enum for WeatherBit units.
 *
 * This enum represents the different units of measurement that can be used
 * when fetching weather data from the WeatherBit API.
 *
 * - M: Metric (Celsius)
 * - S: Scientific (Kelvin)
 * - I: Fahrenheit
 */
export enum EWeatherBitUnits {
  M = 'M',
  S = 'S',
  I = 'I',
}
